"""BookingAPI — business logic sitting on the SQLite schema.

Single class exposing every operation the voice agent needs.  Reads
come back as plain dicts; mutations run inside explicit transactions
(``with conn:``) so activity_log + confirmation_codes + pnrs updates
either all land or all roll back.

Confirmation codes are 8-character uppercase hex generated by
:mod:`secrets` with a DB-UNIQUE retry loop — not hand-rolled, not
LLM-generated, not deterministic.

FastAPI routes in :mod:`examples.frontend_backend_agent.airline.database.server` forward each call to
this class; an MCP transport can replace FastAPI without touching the
service layer or schema.
"""

from __future__ import annotations

import json
import secrets
import sqlite3
from datetime import UTC, date, datetime, timedelta

from loguru import logger

_CODE_RETRIES = 10


def _now() -> str:
    return datetime.now(UTC).replace(microsecond=0).isoformat()


def _gen_pnr() -> str:
    """Generate a fresh 6-character PNR (3 uppercase letters + 3 digits)."""
    import random
    import string

    letters = "".join(random.choice(string.ascii_uppercase) for _ in range(3))
    digits = "".join(random.choice(string.digits) for _ in range(3))
    return letters + digits


